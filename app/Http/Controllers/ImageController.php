<?php

namespace App\Http\Controllers;

use App\Models\Image;
use Illuminate\Http\Request;

class ImageController extends Controller
{
    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $images = Image::all()->forPage(request()->pageNo,request()->numElementPerPage);
        return view('images.index', compact('images'))->with('i',(\request()->input('page', 1)-1)* \request()->numElementPerPage);
    }

    /**
     * Show the form for creating a new resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function create()
    {
        return view('images.create');
    }

    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $request->validate([
            'image' => 'required'
        ]);
//        ddd($request);
        $image = $request->file('image')->store('imageStore',['disk' => 'public']);
//        $image = $request->image->store('imageStore',['disk' => 'public']);
        $data = [];
        $data['image'] = $image;
        Image::create($data);
        return redirect()->route('images.index')
            ->with('success', 'Image create successfully');
    }

    /**
     * Display the specified resource.
     *
     * @param  \App\Models\Image  $image
     * @return \Illuminate\Http\Response
     */
    public function show(Image $image)
    {
        return view('images.show', compact('image'));
    }

    /**
     * Show the form for editing the specified resource.
